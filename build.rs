use std::env;
use std::fs::File;
use std::io::{BufWriter, Write};
use std::path::Path;

fn main() {
    let out_dir = env::var("OUT_DIR").unwrap();
    let path = Path::new(&out_dir).join("codegen.rs");
    let mut file = BufWriter::new(File::create(&path).unwrap());

    let mut icon_map = phf_codegen::Map::new();
    for icon in ICONS {
        let value = format!("icondata::{icon}");
        icon_map.entry(icon, &value);
    }

    write!(
        &mut file,
        "static ICON_MAP: phf::Map<&'static str, icondata::Icon> = {}",
        icon_map.build()
    )
    .unwrap();
    writeln!(&mut file, ";").unwrap();
}

